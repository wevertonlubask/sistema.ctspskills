interface SidebarProps {
  isOpen: boolean;
}

export const Sidebar: React.FC<SidebarProps> = ({ isOpen }) => {
  const user = useAuthStore((state) => state.user);
  const { setSidebarOpen } = useUIStore();
  const userRole = user?.role;

  const filteredItems = navItems.filter(
    (item) => !item.roles || (userRole && item.roles.includes(userRole))
  );

  const handleNavClick = () => {
    // On mobile, close sidebar after navigation
    if (window.innerWidth < 768) {
      setSidebarOpen(false);
    }
  };

  return (
    <aside
      className={clsx(
        'fixed left-0 top-16 h-[calc(100vh-4rem)] overflow-y-auto',
        'bg-white dark:bg-gray-800 border-r border-gray-200 dark:border-gray-700',
        'transition-all duration-300 z-40',
        // Mobile: always w-64, slide in/out with transform
        'w-64',
        isOpen ? 'translate-x-0' : '-translate-x-full',
        // Desktop: override transform (always visible), toggle width
        isOpen ? 'md:translate-x-0 md:w-64' : 'md:translate-x-0 md:w-20',
      )}
    >
      <nav className="mt-6 px-3">
        <ul className="space-y-1">
          {filteredItems.map((item) => (
            <li key={item.path}>
              <NavLink
                to={item.path}
                onClick={handleNavClick}
                className={({ isActive }) =>
                  clsx(
                    'flex items-center px-3 py-2.5 rounded-lg transition-colors',
                    isActive
                      ? 'bg-blue-50 text-blue-600 dark:bg-blue-900/20 dark:text-blue-400'
                      : 'text-gray-700 dark:text-gray-300 hover:bg-gray-100 dark:hover:bg-gray-700'
                  )
                }
              >
                {item.icon}
                <span
                  className={clsx(
                    'ml-3 font-medium whitespace-nowrap transition-all duration-300',
                    // Mobile: always show label when sidebar is open (w-64)
                    // Desktop: show/hide based on sidebar state
                    isOpen ? 'opacity-100 md:opacity-100' : 'opacity-0 md:opacity-0 w-0 overflow-hidden'
                  )}
                >
                  {item.label}
                </span>
              </NavLink>
            </li>
          ))}
        </ul>
      </nav>
    </aside>
  );
};
